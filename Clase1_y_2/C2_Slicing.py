# Explica la sintaxis de slicing inicio:fin[:paso] y da un ejemplo.
# ejemplo con lista
numeros = [10, 20, 30, 40, 50, 60, 70]

print(numeros[1:5])   
print(numeros[:4])     
print(numeros[3:])     
print(numeros[::2])    
print(numeros[::-1])  

#ejemplo con string
palabra = "PYTHON"

print(palabra[0:3])    
print(palabra[2:])    
print(palabra[::-1])   
